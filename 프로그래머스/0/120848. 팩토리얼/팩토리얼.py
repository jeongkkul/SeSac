# n이라는 인자를 넣었을 때, n이 특정 i의 팩토리얼보다 크거나 같아야 한다. 
# 만약 n이 121이라면, i<=n의 조건에 따라  i는 1,2,3,4,5가 된다.
# 이 i의 리스트 중에서 가장 큰 수를 골라 return 하면 된다. 
# 그럼 n과 i!의 관계를 어떻게 파악할 것인가
# n이 정해졌을 때 반복문을 사용해 i를 1부터 계속 넣어보다가, 어느 순간 i!이 n을 넘는 순간이 올 것이다
# 만약 i!이 n을 넘는 그 시점이 온다면, i-1을 출력하면 된다. 
# 반복문 안에 *= j[?] 를 활용한다면 팩토리얼 구현할 수 있을 듯

