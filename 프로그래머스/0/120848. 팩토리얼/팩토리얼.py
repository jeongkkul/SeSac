def solution(n):
    fact = 1   # i! 값을 누적해서 담을 변수 (처음은 1!의 바탕이 되는 1)
    i = 0      # 현재 i 값 (아직 곱하기 전이니 0에서 시작)

    # fact가 n을 넘지 않는 동안 i를 1씩 올리며 곱해 간다
    while fact <= n:
        i += 1         # 다음 i로 이동 (1, 2, 3, ...)
        fact *= i      # fact = i! (이전 값에 i를 곱해 누적)

    # 루프가 끝났다는 건 fact가 방금 n을 넘어섰다는 뜻 → 직전 i가 정답
    return i - 1
